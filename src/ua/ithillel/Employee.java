package ua.ithillel;

public class Employee {

        private String fullName;
        private String email;
        private String post;
        private int age;
        private int phone;

        public Employee(String fullName, String email, String post, int age, int phone) {
            this.fullName = fullName;
            this.email = email;
            this.post = post;
            this.age = age;
            this.phone = phone;
        }
}
